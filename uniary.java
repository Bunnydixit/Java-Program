class uniary
{
public static void main(String args[])
{
int a=5;
System.out.println("value of a is "+a);
System.out.println("value of a after pre-incre is "+(++a));
System.out.println("value of a after post-incre is "+(a++));
System.out.println("value of a is "+a);
System.out.println("value of a after pre-decre is "+(--a));
System.out.println("value of a after post-decre is "+(a--));
System.out.println("value of a  is "+a);

}
}
